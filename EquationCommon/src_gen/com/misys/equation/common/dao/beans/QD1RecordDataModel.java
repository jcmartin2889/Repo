package com.misys.equation.common.dao.beans;

/**
 * QD1 Record data-model.
 * 
 * @author deroset
 * 
 */
public class QD1RecordDataModel extends AbsRecord
{

	private final static String RECORD_NAME = "QD10LF";

	private String systemOptionGroup;
	private String systemOptionGroupDescription;

	/**
	 * This is the unique class id used for serialisation.
	 */
	private static final long serialVersionUID = 1266215593588l;

	/**
	 * Default constructor
	 */
	public QD1RecordDataModel()
	{
		super();
		setEqFileName(RECORD_NAME);

	}

	public QD1RecordDataModel(String systemOptionGroup)
	{
		this.systemOptionGroup = systemOptionGroup;
		setEqFileName(RECORD_NAME);
	}

	// ---getters and setters----//

	public String getSystemOptionGroup()
	{
		return this.systemOptionGroup;
	}
	public void setSystemOptionGroup(String parameter)
	{
		this.systemOptionGroup = parameter;
	}
	public String getSystemOptionGroupDescription()
	{
		return this.systemOptionGroupDescription;
	}
	public void setSystemOptionGroupDescription(String parameter)
	{
		this.systemOptionGroupDescription = parameter;
	}

}
