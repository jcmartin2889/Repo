package com.misys.equation.common.dao.beans;

/**
 * GAF Record data-model.
 * 
 * @author deroset
 * 
 */
public class GAFRecordDataModel extends AbsRecord
{
	private final static String RECORD_NAME = "GAFPF";

	private long sequenceId;
	private String fieldName;
	private String includeAllFields;

	/**
	 * This is the unique class id used for serialisation.
	 */
	private static final long serialVersionUID = 1282102630075l;

	/**
	 * Default constructor
	 */
	public GAFRecordDataModel()
	{
		super();
		setEqFileName(RECORD_NAME);
	}

	// ---getters and setters----//

	public long getSequenceId()
	{
		return this.sequenceId;
	}
	public void setSequenceId(long parameter)
	{
		this.sequenceId = parameter;
	}
	public String getFieldName()
	{
		return this.fieldName;
	}
	public void setFieldName(String parameter)
	{
		this.fieldName = parameter;
	}
	public String getIncludeAllFields()
	{
		return this.includeAllFields;
	}
	public void setIncludeAllFields(String parameter)
	{
		this.includeAllFields = parameter;
	}
}