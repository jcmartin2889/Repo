package com.misys.equation.common.dao.beans;

/**
 * APV Record data-model.
 * 
 * @author deroset
 * 
 */
public class APVRecordDataModel extends AbsRecord
{
	private final static String RECORD_NAME = "APVPF";

	private String apiReference;
	private String apiFileName;
	private String equationApiLevel;
	private String apiProgram;
	private String apiKeyStart;
	private String apiKeyLength;
	private String programRoot;
	private String apiDescription;
	private String conditions;
	private String type;

	/**
	 * This is the unique class id used for serialisation.
	 */
	private static final long serialVersionUID = 1283761830662l;

	/**
	 * Default constructor
	 */
	public APVRecordDataModel()
	{
		super();
		setEqFileName(RECORD_NAME);
	}

	// ---getters and setters----//

	public String getApiReference()
	{
		return this.apiReference;
	}
	public void setApiReference(String parameter)
	{
		this.apiReference = parameter;
	}
	public String getApiFileName()
	{
		return this.apiFileName;
	}
	public void setApiFileName(String parameter)
	{
		this.apiFileName = parameter;
	}
	public String getEquationApiLevel()
	{
		return this.equationApiLevel;
	}
	public void setEquationApiLevel(String parameter)
	{
		this.equationApiLevel = parameter;
	}
	public String getApiProgram()
	{
		return this.apiProgram;
	}
	public void setApiProgram(String parameter)
	{
		this.apiProgram = parameter;
	}
	public String getApiKeyStart()
	{
		return this.apiKeyStart;
	}
	public void setApiKeyStart(String parameter)
	{
		this.apiKeyStart = parameter;
	}
	public String getApiKeyLength()
	{
		return this.apiKeyLength;
	}
	public void setApiKeyLength(String parameter)
	{
		this.apiKeyLength = parameter;
	}
	public String getProgramRoot()
	{
		return this.programRoot;
	}
	public void setProgramRoot(String parameter)
	{
		this.programRoot = parameter;
	}
	public String getApiDescription()
	{
		return this.apiDescription;
	}
	public void setApiDescription(String parameter)
	{
		this.apiDescription = parameter;
	}
	public String getConditions()
	{
		return this.conditions;
	}
	public void setConditions(String parameter)
	{
		this.conditions = parameter;
	}
	public String getType()
	{
		return this.type;
	}
	public void setType(String parameter)
	{
		this.type = parameter;
	}
}