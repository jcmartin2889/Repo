package com.misys.equation.function.beans.valid;

/**
 * A interface implemented by EQ4 data model bean classes to facilitate validation of the bean model
 * 
 * @author perkinj1
 * 
 */
public interface IValidation
{
	/**
	 * Performs validation on the bean
	 * 
	 * @param messageContainer
	 *            an <code>AbsMessageContainer</code> to add the messages to
	 * @param subSet
	 *            a String specifying a validation subset, or null for full validation
	 * @param includeChildren
	 *            Whether to validate child objects as well
	 * @return a boolean, not currently used by any caller.
	 */
	boolean validateBean(MessageContainer messageContainer, String subSet, boolean includeChildren);
}
